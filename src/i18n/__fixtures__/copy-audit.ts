/**
 * 把 forbidden 词聚合成一份扫描列表（测试用）。
 */
export function collectForbidden(): string[] {
  const set = new Set<string>();
  for (const entry of COPY_AUDIT_TABLE) {
    for (const word of entry.forbidden ?? []) {
      set.add(word);
    }
  }
  return Array.from(set);
}

